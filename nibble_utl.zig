//--------------------------------------------------------------//
// Types
//--------------------------------------------------------------//
const Nibble = enum(u8) {
    first = 1,
    second = 2,
    third = 3,
    fourth = 4,
};

//--------------------------------------------------------------//
// Comptime functions
//--------------------------------------------------------------//
fn nibble(data: u16, nib: Nibble) u8 {
    return (data >> (16 - (4 * nib))) & 0xF;
}
